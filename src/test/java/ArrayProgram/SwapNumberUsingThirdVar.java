package ArrayProgram;

public class SwapNumberUsingThirdVar {
	
	
	public static void main(String[] args) {
		
		int a=12;
		int b=13;
		int temp=0;
		temp=a;
		a=b;
		b=temp;
		System.out.println(a+" after swaping the number of a");
		System.out.println(b+" after swaping the number of b");
		
	}
	
	
	
	
	
	
	

}
